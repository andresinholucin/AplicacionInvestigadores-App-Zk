package ec.edu.upse.proyinv.modelo.auxiliares;


public class ObtenerCampos {
	private int id_campo ;
	private String nombre_campo;
	private String tipo_dato;
	
	
	public ObtenerCampos() {
	}
	
	public int getId_campo() {
		return id_campo;
	}
	public void setId_campo(int id_campo) {
		this.id_campo = id_campo;
	}
	public String getNombre_campo() {
		return nombre_campo;
	}
	public void setNombre_campo(String nombre_campo) {
		this.nombre_campo = nombre_campo;
	}
	public String getTipo_dato() {
		return tipo_dato;
	}
	public void setTipo_dato(String tipo_dato) {
		this.tipo_dato = tipo_dato;
	}
	
	
}
